    {
        if (request()->get('type') == 'json') {
            $index = 0;
            $results = [];
            foreach (KebijakanPrivasi::all() as $item) {
                $results[$index] = $item;
                $results[$index]["encryptid"] = encrypt($item->id);
                $index++;
            }
            return response()->json($results);
        }
        return view('admin.pages.kebijakan-privasi.index');
    }

